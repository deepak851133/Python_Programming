#Q5. Write a code for string, int and float input ?
# Taking a string input
string_input = input("Enter a string: ")

# Taking an integer input
int_input = int(input("Enter an integer: "))

# Taking a floating-point input
float_input = float(input("Enter a float number: "))

# Printing the inputs
print("\nYou entered:")
print("String:", string_input)
print("Integer:", int_input)
print("Float:", float_input)
